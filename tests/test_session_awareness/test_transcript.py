def _write(path: Path, entries: list[dict]) -> Path:
    path.write_text("\n".join(json.dumps(e) for e in entries) + "\n")
    return path


def test_parse_delta_full_file(tmp_path):
    entries = [
        _assistant("I propose we ship the widget refactor now."),
        {"type": "mode", "mode": "default"},
        _tool_result(),
        _typed("yes, do that — and add the rollback lever"),
        _typed("/compact"),
    ]
    t = _write(tmp_path / "s.jsonl", entries)
    turns = parse_delta(t, 0, t.stat().st_size)
    assert len(turns) == 1
    turn = turns[0]
    assert turn["user_text"] == "yes, do that — and add the rollback lever"
    assert turn["assistant_snippet"] == "I propose we ship the widget refactor now."
    assert turn["turn_ref"].startswith("u-")
    assert turn["ts"] == "2026-06-30T15:21:06.000Z"


def test_parse_delta_snippet_scans_back_past_noise(tmp_path):
    noise = [{"type": "mode", "mode": "default"} for _ in range(20)]
    entries = [_assistant("the proposal"), *noise, _typed("agreed")]
    t = _write(tmp_path / "s.jsonl", entries)
    turns = parse_delta(t, 0, t.stat().st_size)
    assert turns[0]["assistant_snippet"] == "the proposal"


def test_parse_delta_snippet_absent_beyond_scanback(tmp_path):
    noise = [{"type": "mode", "mode": "default"} for _ in range(60)]
    entries = [_assistant("too far back"), *noise, _typed("agreed")]
    t = _write(tmp_path / "s.jsonl", entries)
    turns = parse_delta(t, 0, t.stat().st_size)
    assert turns[0]["assistant_snippet"] == ""


def test_parse_delta_snippet_skips_sidechain_assistant(tmp_path):
    entries = [
        _assistant("real assistant text"),
        _assistant("subagent chatter", isSidechain=True),
        _typed("ok go"),
    ]
    t = _write(tmp_path / "s.jsonl", entries)
    turns = parse_delta(t, 0, t.stat().st_size)
    assert turns[0]["assistant_snippet"] == "real assistant text"


def test_parse_delta_byte_window_and_torn_line(tmp_path):
    """A start byte landing mid-line skips the torn line; entries past
    end_byte are excluded."""
    early = _typed("early prompt", ts="2026-06-30T15:00:00.000Z")
    mid = _typed("mid prompt", ts="2026-06-30T15:10:00.000Z")
    late = _typed("late prompt", ts="2026-06-30T15:20:00.000Z")
    t = _write(tmp_path / "s.jsonl", [early, mid, late])
    raw = t.read_bytes()
    early_end = raw.find(b"\n") + 1
    mid_end = raw.find(b"\n", early_end) + 1
    # start inside the first line (torn), end after the second line
    turns = parse_delta(t, 5, mid_end)
    assert [x["user_text"] for x in turns] == ["mid prompt"]
    # clean start at a line boundary sees the rest
    turns = parse_delta(t, early_end, t.stat().st_size)
    assert [x["user_text"] for x in turns] == ["mid prompt", "late prompt"]


def test_parse_delta_caps_user_text(tmp_path):
    t = _write(tmp_path / "s.jsonl", [_typed("x" * 5000)])
    turns = parse_delta(t, 0, t.stat().st_size, max_user_chars=100)
    assert len(turns[0]["user_text"]) == 100


def test_parse_delta_turn_ref_falls_back_to_timestamp(tmp_path):
    entry = _typed("no uuid prompt")
    del entry["uuid"]
    t = _write(tmp_path / "s.jsonl", [entry])
    turns = parse_delta(t, 0, t.stat().st_size)
    assert turns[0]["turn_ref"] == "2026-06-30T15:21:06.000Z"


def test_parse_delta_tolerates_garbage_lines(tmp_path):
    t = tmp_path / "s.jsonl"
    t.write_text("not json\n[1,2]\n" + json.dumps(_typed("hi")) + "\n")
    turns = parse_delta(t, 0, t.stat().st_size)
    assert [x["user_text"] for x in turns] == ["hi"]
