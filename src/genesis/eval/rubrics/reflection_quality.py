"""reflection_quality rubric.

Grades the quality of a deep reflection observation — an insight
generated by a Deep or Strategic reflection CC session. Quality is
measured across four dimensions: specificity, actionability, novelty,
and grounding.

This rubric supports the DSPy prompt optimization pipeline (V4) by
providing a calibrated quality signal for training data labeling.
The golden set lives outside the repo (``~/.genesis/output/``) because
it contains private system context.

The rubric is loaded with the package via ``eval.rubrics.__init__``.
"""

from __future__ import annotations

from genesis.eval.rubrics import Rubric, register_rubric

_PROMPT = """\
You are grading the *quality* of a cognitive reflection observation
produced by an autonomous AI agent's deep reflection cycle.

A reflection observation is a single insight extracted from a longer
reflection session. It summarizes something the agent noticed about its
own operation, its user's activity, or its system state.

Grade on four dimensions, then produce a composite score:

1. **Specificity (0.25 weight):** Does the observation name concrete
   entities — files, functions, error messages, PR numbers, metric
   values, timestamps? Or is it vague ("system is stressed", "things
   are degrading")? Concrete references score high; generic framing
   scores low.

2. **Actionability (0.35 weight):** Does the observation contain or
   imply a concrete next step — a bug to fix, a config to change, a
   follow-up to create, a memory operation to perform, a proposal to
   make? Observations that merely describe state without suggesting
   what to do about it score low. This is the most important dimension.

3. **Novelty (0.20 weight):** Does this observation surface something
   not already obvious from routine monitoring? Paraphrasing known
   system state ("container memory at 65%, within normal range") scores
   low. Identifying a non-obvious pattern, correlation, or emerging
   issue scores high.

4. **Grounding (0.20 weight):** Does the observation reference actual
   system state, logs, events, or data that can be verified? Or does
   it make claims without evidence? Observations that cite specific
   data points score high; speculation presented as fact scores low.

Context about the system state when this reflection ran:
{session_context}

Reflection observation to grade:
{actual}

Reference label (FYI only):
{expected}

Score the overall quality from 0.0 (noise — vague, non-actionable,
repetitive, ungrounded) through 0.5 (marginal — some specificity but
limited actionability) to 1.0 (high-quality — specific, actionable,
novel, grounded). Apply the dimension weights above.

Respond with ONLY a JSON object, no markdown fences, no prose:
{{"score": <float 0.0-1.0>, "rationale": "<one sentence>"}}"""


REFLECTION_QUALITY = Rubric(
    name="reflection_quality",
    version="1.0.0",
    description=(
        "Grades quality of deep reflection observations on four "
        "dimensions: specificity (0.25), actionability (0.35), "
        "novelty (0.20), grounding (0.20). Foundation rubric for "
        "DSPy prompt optimization training data labeling."
    ),
    prompt_template=_PROMPT,
    pass_threshold=0.6,
    extra_placeholders=("session_context",),
)


register_rubric(REFLECTION_QUALITY)
